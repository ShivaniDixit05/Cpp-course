#include <iostream>
using namespace std;

int main()
{
    //integer 
    int age;
    age = 36;
    cout << age << endl;

    //Char
    char grade;
    grade = 'a';
    cout << grade << endl;

    // float
    float num;
    num = 345.987646f;
    cout << num << endl;

    // bool  true -> 1 and false -> 0
    bool isSafe;
    isSafe = false;
    cout << isSafe << endl;

    // double
    double Num;
    Num = 9876.987654321;
    cout << Num << endl;

    return 0;

}
